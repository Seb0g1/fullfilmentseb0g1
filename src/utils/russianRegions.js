// Получить список всех регионов
export function getRegions() {
  return Object.keys(russianRegions)
}

// Получить города для региона
export function getCities(region) {
  return russianRegions[region] || []
}

// Получить регион по городу
export function getRegionByCity(city) {
  for (const [region, cities] of Object.entries(russianRegions)) {
    if (cities.includes(city)) {
      return region
    }
  }
  return null
}

